var burger = document.querySelector(".burger");
var body = document.querySelector(".body");
var nav = document.querySelector(".header__nav");
var first1 = document.querySelector(".first-1");
var second1 = document.querySelector(".second-1");
var btn1 = document.querySelector(".btn-1");
var list1 = document.querySelector(".list-1");

burger.addEventListener("click", function () {
  burger.classList.toggle("burger--active");
  nav.classList.toggle("header__nav--active");
  body.classList.toggle("body--active");
})

btn1.addEventListener("click", function () {
  btn1.classList.toggle("directors__list-btn--active");
  first1.classList.toggle("first--active");
  second1.classList.toggle("second--active");
  list1.classList.toggle("directors__list-item--active");
})
